CREATE TABLE IF NOT EXISTS prematch_predictions (
    id                   SERIAL PRIMARY KEY,
    fixture_id           INT NOT NULL,
    league_id            INT,
    ts_created           TIMESTAMPTZ NOT NULL DEFAULT NOW(),
    kickoff              TIMESTAMPTZ,
    home_team            TEXT,
    away_team            TEXT,
    home_team_id         INT,
    away_team_id         INT,
    home_odds            FLOAT,
    draw_odds            FLOAT,
    away_odds            FLOAT,
    p_home_implied       FLOAT,
    p_draw_implied       FLOAT,
    p_away_implied       FLOAT,
    p_home_composite     FLOAT,
    p_draw_composite     FLOAT,
    p_away_composite     FLOAT,
    recommended_outcome  TEXT,
    confidence           FLOAT,
    form_home            FLOAT,
    form_away            FLOAT,
    h2h_home_rate        FLOAT,
    form_home_str        TEXT,
    form_away_str        TEXT,
    telegram_sent        BOOLEAN NOT NULL DEFAULT FALSE,
    UNIQUE (fixture_id)
);
